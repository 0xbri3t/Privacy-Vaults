import {
  AccountTypeEnum,
  AuthProvider,
  getDefaultConfig,
  OpenfortProvider,
  RecoveryMethod,
} from '@openfort/react'
import { QueryClient, QueryClientProvider } from '@tanstack/react-query'
import { useState } from 'react'
import { baseSepolia } from 'viem/chains'
import { createConfig, WagmiProvider } from 'wagmi'

const wagmiConfig = createConfig(
  getDefaultConfig({
    appName: 'Openfort x402 demo',
    chains: [baseSepolia],
    walletConnectProjectId: import.meta.env.VITE_WALLET_CONNECT_PROJECT_ID,
  }),
)

export function OpenfortProviders({ children }: { children: React.ReactNode }) {
  const [queryClient] = useState(() => new QueryClient())
  return (
    <WagmiProvider config={wagmiConfig}>
      <QueryClientProvider client={queryClient}>
        <OpenfortProvider
          debugMode
          publishableKey={import.meta.env.VITE_OPENFORT_PUBLISHABLE_KEY!}
          // Set the wallet configuration. In this example, we will be using the embedded signer.
          walletConfig={{
            shieldPublishableKey: import.meta.env
              .VITE_OPENFORT_SHIELD_PUBLISHABLE_KEY!,
            ethereumProviderPolicyId: import.meta.env.VITE_OPENFORT_POLICY_ID,
            createEncryptedSessionEndpoint: import.meta.env
              .VITE_CREATE_ENCRYPTED_SESSION_ENDPOINT,
            accountType: AccountTypeEnum.EOA,
          }}
          uiConfig={{
            authProviders: [
              AuthProvider.EMAIL_OTP,
              AuthProvider.GUEST,
              AuthProvider.GOOGLE,
              AuthProvider.WALLET,
            ],
            walletRecovery: {
              defaultMethod: RecoveryMethod.PASSKEY,
            },
          }}
        >
          {children}
        </OpenfortProvider>
      </QueryClientProvider>
    </WagmiProvider>
  )
}
